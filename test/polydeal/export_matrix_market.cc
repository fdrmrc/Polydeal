#include <deal.II/base/utilities.h>

#include <agglomeration_handler.h>
#include <poly_utils.h>

#include <iostream>


void
test(TrilinosWrappers::SparseMatrix &m)
{
  unsigned int counter = 0;
  for (unsigned int i = 0; i < m.m(); ++i)
    for (unsigned int j = 0; j < m.m(); ++j)
      if ((i + 2 * j + 1) % 3 == 0)
        {
          m.set(i, j, i * j * .5 + .5);
          ++counter;
        }

  m.compress(VectorOperation::insert);

  const std::string filename{"exported_matrix"};
  const std::string matrix_name{"test_matrix"};
  PolyUtils::write_to_matrix_market_format(filename, matrix_name, m);
  std::cout << "OK" << std::endl;
}



int
main(int argc, char **argv)
{
  Utilities::MPI::MPI_InitFinalize mpi_initialization(argc, argv, 1);

  try
    {
      {
        TrilinosWrappers::SparseMatrix m(10U, 10U, 10U);
        test(m);
      }
    }
  catch (const std::exception &exc)
    {
      std::cerr << std::endl
                << std::endl
                << "----------------------------------------------------"
                << std::endl;
      std::cerr << "Exception on processing: " << std::endl
                << exc.what() << std::endl
                << "Aborting!" << std::endl
                << "----------------------------------------------------"
                << std::endl;

      return 1;
    }
  catch (...)
    {
      std::cerr << std::endl
                << std::endl
                << "----------------------------------------------------"
                << std::endl;
      std::cerr << "Unknown exception!" << std::endl
                << "Aborting!" << std::endl
                << "----------------------------------------------------"
                << std::endl;
      return 1;
    };
}
